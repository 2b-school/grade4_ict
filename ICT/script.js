startBtn.addEventListener('click', () => {
    if (studentNameInput.value.trim() === '') {
        studentNameInput.focus();
        studentNameInput.style.border = "2px solid red";
        setTimeout(() => studentNameInput.style.border = "", 1000);
        return;
    }
    studentName = studentNameInput.value.trim();
    selectedUnitKey = unitSelect.value;
    selectedLessonKey = lessonSelect.disabled ? "all" : lessonSelect.value;
    startGame();
});

// دالة بدء اللعبة (معدلة لتعتمد على currentGradeData)
function startGame() {
    currentQuestion = 0;
    score = 0;
    let allWords = [];

    if (selectedUnitKey === "all") {
        currentGradeData.forEach(unit => {
            unit.lessons.forEach(lesson => {
                allWords.push(...lesson.words);
            });
        });
    } else {
        const chosenUnit = currentGradeData.find(u => u.unitName === selectedUnitKey);
        if (chosenUnit) {
            if (selectedLessonKey === "all") {
                chosenUnit.lessons.forEach(lesson => {
                    allWords.push(...lesson.words);
                });
            } else {
                const chosenLesson = chosenUnit.lessons.find(l => l.lessonName === selectedLessonKey);
                if (chosenLesson) {
                    allWords.push(...chosenLesson.words);
                }
            }
        }
    }

    if (allWords.length === 0) {
        alert("عذراً، لم يتم العثور على كلمات لهذا الاختيار.");
        return;
    }

    shuffledWords = shuffleArray([...allWords]);
    if (shuffledWords.length > 30) {
        shuffledWords = shuffledWords.slice(0, 30);
    }

    welcomePage.style.display = 'none';
    testPage.style.display = 'block';
    resultPage.style.display = 'none';

    studentNameDisplay.textContent = studentName;
    currentScoreElement.textContent = score;

    showQuestion();
}

// دالة عرض السؤال
function showQuestion() {
    if (currentQuestion >= shuffledWords.length) {
        showResults();
        return;
    }

    const progressPercent = ((currentQuestion) / shuffledWords.length) * 100;
    progressBar.style.width = `${progressPercent}%`;
    progressText.textContent = `${currentQuestion + 1}/${shuffledWords.length}`;

    const word = shuffledWords[currentQuestion];
    correctAnswer = isEnglishToArabic ? word.arabic : word.english;
    questionElement.textContent = isEnglishToArabic ? word.english : word.arabic;
    
    feedbackMessage.textContent = '';
    feedbackMessage.className = 'feedback-message';

    if (gameMode === 'multiple') {
        typingContainer.style.display = 'none';
        optionsContainer.style.display = 'grid';
        showMultipleChoiceOptions(word);
    } else {
        optionsContainer.style.display = 'none';
        typingContainer.style.display = 'flex';
        answerInput.value = '';
        answerInput.focus();
        checkAnswerBtn.style.display = 'block';
    }

    nextBtn.style.display = 'none';
}

// دالة عرض الخيارات المتعددة
function showMultipleChoiceOptions(correctWord) {
    optionsContainer.innerHTML = '';
    
    let otherWords = shuffledWords.filter(w => w.english !== correctWord.english);
    otherWords = shuffleArray(otherWords).slice(0, 3);
    
    const options = [correctWord, ...otherWords];
    const shuffledOptions = shuffleArray(options);
    
    shuffledOptions.forEach(option => {
        const btn = document.createElement('button');
        btn.className = 'option-btn'; 
        btn.textContent = isEnglishToArabic ? option.arabic : option.english;
        
        btn.onclick = () => {
            Array.from(optionsContainer.children).forEach(b => b.disabled = true);
            
            if (option.english === correctWord.english) {
                btn.style.backgroundColor = 'var(--correct-color, #4CAF50)';
                correctSound.play();
                score++;
                currentScoreElement.textContent = score;
                feedbackMessage.textContent = 'إجابة صحيحة! أحسنت!';
                feedbackMessage.style.color = 'var(--correct-color, #4CAF50)';
            } else {
                btn.style.backgroundColor = 'var(--wrong-color, #F44336)';
                wrongSound.play();
                feedbackMessage.textContent = 'إجابة خاطئة! الإجابة هي: ' + (isEnglishToArabic ? correctWord.arabic : correctWord.english);
                feedbackMessage.style.color = 'var(--wrong-color, #F44336)';
            }
            nextBtn.style.display = 'block';
        };
        optionsContainer.appendChild(btn);
    });
}

// دالة التحقق من الإجابة الكتابية
function checkTypedAnswer() {
    const userAnswer = answerInput.value.trim().toLowerCase();
    if (userAnswer === '') return;

    const correctLower = correctAnswer.toLowerCase();
    
    answerInput.disabled = true;
    checkAnswerBtn.style.display = 'none';

    if (userAnswer === correctLower) {
        answerInput.style.borderColor = 'green';
        correctSound.play();
        score++;
        currentScoreElement.textContent = score;
        feedbackMessage.textContent = 'إجابة صحيحة! أحسنت!';
        feedbackMessage.style.color = 'green';
    } else {
        answerInput.style.borderColor = 'red';
        wrongSound.play();
        feedbackMessage.textContent = `إجابة خاطئة! الصحيح هو: ${correctAnswer}`;
        feedbackMessage.style.color = 'red';
    }
    
    nextBtn.style.display = 'block';
}

// دالة عرض النتائج
function showResults() {
    testPage.style.display = 'none';
    resultPage.style.display = 'block';
    
    finalScoreElement.textContent = `${score} / ${shuffledWords.length}`;
    studentResultName.textContent = studentName;
    
    const percentage = (score / shuffledWords.length) * 100;
    if (percentage >= 80) {
        resultTitle.textContent = "ممتاز!";
        resultMessage.textContent = "لقد أبليت بلاءً حسناً في الاختبار.";
        winSound.play();
    } else if (percentage >= 50) {
        resultTitle.textContent = "جيد جداً";
        resultMessage.textContent = "يمكنك تحقيق نتيجة أفضل بالمزيد من التدريب.";
    } else {
        resultTitle.textContent = "حاول مرة أخرى";
        resultMessage.textContent = "لا تستسلم، راجع الكلمات وحاول من جديد!";
    }
}

// دالة خلط المصفوفات
function shuffleArray(array) {
    let currentIndex = array.length, randomIndex;
    while (currentIndex !== 0) {
        randomIndex = Math.floor(Math.random() * currentIndex);
        currentIndex--;
        [array[currentIndex], array[randomIndex]] = [array[randomIndex], array[currentIndex]];
    }
    return array;
}

