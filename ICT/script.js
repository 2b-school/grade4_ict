// دالة ملء قائمة الصفوف (جديدة)
function populateGradeSelect() {
    gradeSelect.innerHTML = ''; // تفريغ القائمة
    // المرور على كل الصفوف الموجودة في قاموس ictGrades
    for (const grade in ictGrades) {
        const option = document.createElement('option');
        option.value = grade;
        option.textContent = grade;
        gradeSelect.appendChild(option);
    }
    // تحديث البيانات بناءً على الصف الأول الافتراضي
    updateCurrentGradeData();
}

// دالة تحديث البيانات عند تغيير الصف (جديدة)
function updateCurrentGradeData() {
    const selectedGrade = gradeSelect.value;
    currentGradeData = ictGrades[selectedGrade]; // جلب وحدات الصف المختار
    populateUnitSelect(); // تحديث قائمة الوحدات بناءً على الصف الجديد
}

// دالة ملء قائمة الوحدات (معدلة لتعتمد على currentGradeData)
function populateUnitSelect() {
    unitSelect.innerHTML = '<option value="all">كل الوحدات</option>';
    currentGradeData.forEach(unit => {
        const option = document.createElement('option');
        option.value = unit.unitName;
        option.textContent = unit.unitName;
        unitSelect.appendChild(option);
    });
    populateLessonSelect(); // تحديث قائمة الدروس تلقائياً
}

// دالة ملء قائمة الدروس (معدلة لتعتمد على currentGradeData)
function populateLessonSelect() {
    lessonSelect.innerHTML = '<option value="all">كل الدروس</option>';
    if (unitSelect.value === "all") {
        lessonSelect.disabled = true;
        return;
    }
    lessonSelect.disabled = false;
    const unit = currentGradeData.find(u => u.unitName === unitSelect.value);
    if (unit) {
        unit.lessons.forEach(lesson => {
            const option = document.createElement('option');
            option.value = lesson.lessonName;
            option.textContent = lesson.lessonName;
            lessonSelect.appendChild(option);
        });
    }
}

