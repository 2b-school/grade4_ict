function startGame() {
    currentQuestion = 0;
    score = 0;
    
    let allWords = [];
    
    if (selectedUnitKey === "all") {
        ictUnits.forEach(unit => {
            unit.lessons.forEach(lesson => {
                allWords = allWords.concat(lesson.words);
            });
        });
    } else {
        const chosenUnit = ictUnits.find(u => u.unitName === selectedUnitKey);
        if (chosenUnit) {
            if (selectedLessonKey === "all") {
                chosenUnit.lessons.forEach(lesson => {
                    allWords = allWords.concat(lesson.words);
                });
            } else {
                const chosenLesson = chosenUnit.lessons.find(l => l.lessonName === selectedLessonKey);
                if (chosenLesson) allWords = chosenLesson.words;
            }
        }
    }
    
    // التعديل الجديد: يختار 30 سؤال عشوائي فقط
    if (allWords.length > 30) {
        allWords = shuffleArray([...allWords]).slice(0, 30);
    }
    
    shuffledWords = shuffleArray([...allWords]);
    
    welcomePage.style.display = 'none';
    testPage.style.display = 'block';
    resultPage.style.display = 'none';
    
    studentNameDisplay.textContent = studentName;
    currentScoreElement.textContent = score;
    
    showQuestion();
}

